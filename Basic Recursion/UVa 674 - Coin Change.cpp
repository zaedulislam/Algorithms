int dp[10][10000], make;

int coinChange(int I, int amount)
{
	if(I >= 5){
		if(amount == 0)
			return 1;
		else return 0;
	}
	if(dp[I][amount] != -1) return dp[I][amount];
	int ret1 = 0, ret2 = 0;
	if(amount - coin[I] >= 0)
		ret1 = coinChange(I, amount - coin[I]);

	ret2 = coinChange(I + 1, amount);
	return dp[I][amount] = ret1 + ret2;
}

